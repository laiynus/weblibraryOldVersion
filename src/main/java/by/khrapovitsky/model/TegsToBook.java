package by.khrapovitsky.model;

public class TegsToBook {
	
	private int id;
	private int book;
	private int teg;
	
	public int getId() {
		return id;
	}
	
	public void setId(int id) {
		this.id = id;
	}
	
	public int getBook() {
		return book;
	}
	
	public void setBook(int book) {
		this.book = book;
	}
	
	public int getTeg() {
		return teg;
	}
	
	public void setTeg(int teg) {
		this.teg = teg;
	}
	
	
}
