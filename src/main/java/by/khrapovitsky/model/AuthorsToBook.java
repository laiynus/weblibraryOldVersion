package by.khrapovitsky.model;

public class AuthorsToBook {
	
	private int id;
	private int book;
	private int author;
	
	public int getId() {
		return id;
	}
	
	public void setId(int id) {
		this.id = id;
	}
	
	public int getBook() {
		return book;
	}
	
	public void setBook(int book) {
		this.book = book;
	}
	
	public int getAuthor() {
		return author;
	}
	
	public void setAuthor(int author) {
		this.author = author;
	}
	
	
}
